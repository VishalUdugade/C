#include<stdio.h>
int main()
{
    int i=0,j=0;

    printf("\t table from 5 to 10\n");
    printf("\t====================================================================\n");

    for(i=5;i<=10;i++)
    {
        for(j=1;j<=10;j++)
        {
            printf("\t %d \n",i*j);
        }
        printf(" \n ");
    }
    printf("======================================================================\n");
    return(0);


}
